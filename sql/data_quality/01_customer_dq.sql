-- Check 1 — Duplicate Customers

SELECT
CUSTOMER_ID,
COUNT(*) AS RECORD_COUNT

FROM CUSTOMER_SILVER

GROUP BY CUSTOMER_ID

HAVING COUNT(*) > 1;

-- Check 2 — Missing Email

SELECT *

FROM CUSTOMER_SILVER

WHERE EMAIL IS NULL

OR EMAIL='';

-- Check 3 — Invalid Country

SELECT *

FROM CUSTOMER_SILVER

WHERE COUNTRY IS NULL;

-- Check 4 — Future Created Date

SELECT *

FROM CUSTOMER_SILVER

WHERE CREATED_DATE > CURRENT_DATE;