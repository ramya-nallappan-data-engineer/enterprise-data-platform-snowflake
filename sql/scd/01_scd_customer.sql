-- Close existing record

UPDATE GOLD.DIM_CUSTOMER

SET

EFFECTIVE_TO = CURRENT_DATE - 1,

IS_CURRENT = FALSE

FROM SILVER.CUSTOMER_SILVER S

WHERE DIM_CUSTOMER.CUSTOMER_ID=S.CUSTOMER_ID

AND DIM_CUSTOMER.IS_CURRENT=TRUE

AND
(
DIM_CUSTOMER.FIRST_NAME<>S.FIRST_NAME
OR
DIM_CUSTOMER.LAST_NAME<>S.LAST_NAME
OR
DIM_CUSTOMER.EMAIL<>S.EMAIL
OR
DIM_CUSTOMER.PHONE<>S.PHONE
OR
DIM_CUSTOMER.COUNTRY<>S.COUNTRY
);

INSERT INTO GOLD.DIM_CUSTOMER
(
CUSTOMER_ID,
FIRST_NAME,
LAST_NAME,
EMAIL,
PHONE,
COUNTRY,
EFFECTIVE_FROM,
EFFECTIVE_TO,
IS_CURRENT
)

SELECT

S.CUSTOMER_ID,

S.FIRST_NAME,

S.LAST_NAME,

S.EMAIL,

S.PHONE,

S.COUNTRY,

CURRENT_DATE,

'9999-12-31',

TRUE

FROM SILVER.CUSTOMER_SILVER S

LEFT JOIN GOLD.DIM_CUSTOMER D

ON S.CUSTOMER_ID=D.CUSTOMER_ID

AND D.IS_CURRENT=TRUE

WHERE

D.CUSTOMER_ID IS NULL

OR

(
D.FIRST_NAME<>S.FIRST_NAME
OR
D.LAST_NAME<>S.LAST_NAME
OR
D.EMAIL<>S.EMAIL
OR
D.PHONE<>S.PHONE
OR
D.COUNTRY<>S.COUNTRY
);