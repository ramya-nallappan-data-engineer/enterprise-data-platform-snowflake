USE DATABASE TRAVEL_DW;
USE SCHEMA GOLD;

MERGE INTO DIM_HOTEL T

USING SILVER.HOTEL_SILVER S

ON T.HOTEL_ID = S.HOTEL_ID

WHEN MATCHED THEN
UPDATE SET

    T.HOTEL_NAME     = S.HOTEL_NAME,
    T.CITY           = S.CITY,
    T.COUNTRY        = S.COUNTRY,
    T.STAR_RATING    = S.STAR_RATING,
    T.IS_ACTIVE      = TRUE

WHEN NOT MATCHED THEN

INSERT
(
    HOTEL_ID,
    HOTEL_NAME,
    CITY,
    COUNTRY,
    STAR_RATING,
    IS_ACTIVE
)

VALUES
(
    S.HOTEL_ID,
    S.HOTEL_NAME,
    S.CITY,
    S.COUNTRY,
    S.STAR_RATING,
    TRUE
);