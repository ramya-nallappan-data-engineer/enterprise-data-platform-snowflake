MERGE INTO HOTEL_SILVER T

USING HOTEL_BRONZE S

ON T.HOTEL_ID = S.HOTEL_ID

WHEN MATCHED THEN
UPDATE SET

    T.HOTEL_NAME    = S.HOTEL_NAME,
    T.CITY          = S.CITY,
    T.COUNTRY       = S.COUNTRY,
    T.STAR_RATING   = S.STAR_RATING,
    T.UPDATED_DATE  = CURRENT_TIMESTAMP()

WHEN NOT MATCHED THEN

INSERT
(
HOTEL_ID,
HOTEL_NAME,
CITY,
COUNTRY,
STAR_RATING,
CREATED_DATE,
UPDATED_DATE
)

VALUES
(
S.HOTEL_ID,
S.HOTEL_NAME,
S.CITY,
S.COUNTRY,
S.STAR_RATING,
CURRENT_TIMESTAMP(),
CURRENT_TIMESTAMP()
);