INSERT INTO CUSTOMER_SILVER
SELECT

CUSTOMER_ID,

UPPER(TRIM(FIRST_NAME)),

UPPER(TRIM(LAST_NAME)),

LOWER(TRIM(EMAIL)),

PHONE,

UPPER(TRIM(COUNTRY)),

CREATED_DATE,

UPDATED_DATE,

SOURCE_SYSTEM,

CURRENT_TIMESTAMP(),

BATCH_ID

FROM CUSTOMER_BRONZE

WHERE RECORD_STATUS='ACTIVE';

