CREATE OR REPLACE PROCEDURE SP_LOAD_CUSTOMER()

RETURNS STRING

LANGUAGE SQL

AS
$$

BEGIN

MERGE INTO CUSTOMER_SILVER T

USING CUSTOMER_BRONZE S

ON T.CUSTOMER_ID = S.CUSTOMER_ID

WHEN MATCHED THEN
UPDATE SET

    T.FIRST_NAME   = S.FIRST_NAME,
    T.LAST_NAME    = S.LAST_NAME,
    T.EMAIL        = S.EMAIL,
    T.PHONE        = S.PHONE,
    T.COUNTRY      = S.COUNTRY,
    T.UPDATED_DATE = CURRENT_TIMESTAMP()

WHEN NOT MATCHED THEN

INSERT
(
CUSTOMER_ID,
FIRST_NAME,
LAST_NAME,
EMAIL,
PHONE,
COUNTRY,
CREATED_DATE,
UPDATED_DATE
)

VALUES
(
S.CUSTOMER_ID,
S.FIRST_NAME,
S.LAST_NAME,
S.EMAIL,
S.PHONE,
S.COUNTRY,
CURRENT_TIMESTAMP(),
CURRENT_TIMESTAMP()
);

RETURN 'CUSTOMER LOAD COMPLETED';

END;

$$;