USE DATABASE TRAVEL_DW;

USE SCHEMA SILVER;

CREATE OR REPLACE PROCEDURE SP_LOAD_SILVER()

RETURNS STRING

LANGUAGE SQL

EXECUTE AS OWNER

AS
$$

BEGIN

    CALL SP_LOAD_CUSTOMER();

    CALL SP_LOAD_HOTEL();

    CALL SP_LOAD_BOOKING();

    CALL SP_LOAD_PAYMENT();

    CALL SP_LOAD_REVIEW();

    RETURN 'SILVER LOAD COMPLETED';

END;

$$;