CREATE OR REPLACE PROCEDURE SP_LOAD_PAYMENT()

RETURNS STRING

LANGUAGE SQL

AS
$$

BEGIN

MERGE INTO PAYMENT_SILVER T

USING PAYMENT_BRONZE S

ON T.PAYMENT_ID = S.PAYMENT_ID

WHEN MATCHED THEN
UPDATE SET

    T.BOOKING_ID      = S.BOOKING_ID,
    T.PAYMENT_METHOD  = S.PAYMENT_METHOD,
    T.PAYMENT_STATUS  = S.PAYMENT_STATUS,
    T.PAYMENT_AMOUNT  = S.PAYMENT_AMOUNT,
    T.UPDATED_DATE    = CURRENT_TIMESTAMP()

WHEN NOT MATCHED THEN

INSERT
(
PAYMENT_ID,
BOOKING_ID,
PAYMENT_METHOD,
PAYMENT_STATUS,
PAYMENT_AMOUNT,
CREATED_DATE,
UPDATED_DATE
)

VALUES
(
S.PAYMENT_ID,
S.BOOKING_ID,
S.PAYMENT_METHOD,
S.PAYMENT_STATUS,
S.PAYMENT_AMOUNT,
CURRENT_TIMESTAMP(),
CURRENT_TIMESTAMP()
);

RETURN 'PAYMENT LOAD COMPLETED';

END;

$$;