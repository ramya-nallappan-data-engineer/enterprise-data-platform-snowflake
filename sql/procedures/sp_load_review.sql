CREATE OR REPLACE PROCEDURE SP_LOAD_REVIEW()

RETURNS STRING

LANGUAGE SQL

AS
$$

BEGIN

MERGE INTO REVIEW_SILVER T

USING REVIEW_BRONZE S

ON T.REVIEW_ID = S.REVIEW_ID

WHEN MATCHED THEN
UPDATE SET

    T.BOOKING_ID     = S.BOOKING_ID,
    T.CUSTOMER_ID    = S.CUSTOMER_ID,
    T.RATING         = S.RATING,
    T.REVIEW_TEXT    = S.REVIEW_TEXT,
    T.UPDATED_DATE   = CURRENT_TIMESTAMP()

WHEN NOT MATCHED THEN

INSERT
(
REVIEW_ID,
BOOKING_ID,
CUSTOMER_ID,
RATING,
REVIEW_TEXT,
CREATED_DATE,
UPDATED_DATE
)

VALUES
(
S.REVIEW_ID,
S.BOOKING_ID,
S.CUSTOMER_ID,
S.RATING,
S.REVIEW_TEXT,
CURRENT_TIMESTAMP(),
CURRENT_TIMESTAMP()
);

RETURN 'REVIEW LOAD COMPLETED';

END;

$$;